package com.interview.algorithms;

public class Range {
	private int i;
	private int j;
	Range(int i, int j){
		this.setI(i);
		this.setJ(j);
	}
	public int getI() {
		return i;
	}
	public void setI(int i) {
		this.i = i;
	}
	public int getJ() {
		return j;
	}
	public void setJ(int j) {
		this.j = j;
	}
}
